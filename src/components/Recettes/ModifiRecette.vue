<template>
  
  <div class="container mt-5">
    
    <!-- Boutons de changement de langue -->
    <div class="mb-4 d-flex justify-content-end">
      <button @click="changeLanguage('en')" class="btn btn-primary me-2">English</button>
      <button @click="changeLanguage('fr')" class="btn btn-primary">Français</button>
    </div>
    <form
      @submit.prevent="handleUpdateRecette"
      class="formulaire form mb-5 shadow p-3 mb-5 bg-body rounded"
    >
      <div class="mb-3">
        <label for="titre" class="form-label">{{ $t('title') }} :</label>
        <input
          type="text"
          class="form-control"
          v-model="title"
          id="titre"
          required
        />
      </div>
      <div class="mb-3">
        <label for="ingredient" class="form-label">{{ $t('ingredients') }} :</label>
        <textarea
          class="form-control"
          v-model="ingredient"
          id="ingredient"
          required
        ></textarea>
      </div>
      <div class="mb-3">
        <label for="type" class="form-label">{{ $t('type') }} :</label>
        <select class="input form-select" v-model="type" id="type" required>
          <option value="Entrée">{{ $t('entry') }}</option>
          <option value="Plat">{{ $t('main_course') }}</option>
          <option value="Dessert">{{ $t('dessert') }}</option>
        </select>
      </div>
      <button class="clr btn mt-3 mb-4 me-3 text-white">{{ $t('save') }}</button>
      <RouterLink
        class="list text-decoration-none text-white me-5 fw-bold"
        to="/listrecette"
      >
        <button class="btn btn-danger mt-3 mb-4">{{ $t('cancel') }}</button>
      </RouterLink>
    </form>
  </div>
</template>

<script setup>
import { useGestionStore } from "@/stores/gestion";
import { useRoute, useRouter } from "vue-router";
import { ref, onMounted } from "vue";

import { getCurrentInstance } from 'vue';

const { proxy } = getCurrentInstance();

const changeLanguage = (locale) => {
  proxy.$i18n.locale = locale;
};
const store = useGestionStore();
const router = useRouter();
const route = useRoute();

const title = ref("");
const ingredient = ref("");
const type = ref("");

const id = Number(route.params.id);

onMounted(() => {
  const recette = store.recettes.find((recette) => recette.id === id);
  if (recette) {
    title.value = recette.title;
    ingredient.value = recette.ingredient;
    type.value = recette.type;
  }
});

const handleUpdateRecette = () => {
  const updatedRecette = {
    id,
    title: title.value,
    ingredient: ingredient.value,
    type: type.value,
  };

  store.updateRecette(updatedRecette);
  router.push("/listrecette");
};
</script>

<style scoped>
.clr {
  background-color: #343a40;
}
.clr:hover {
  background-color: #24272a;
}
.formulaire {
  width: 50%;
  border-radius: 10px;
  padding: 20px;
